        (function() {
            var popupOverlay = document.getElementById('leadPopupOverlay');
            var popupClose = document.getElementById('leadPopupClose');

            if (!popupOverlay || !popupClose) {
                return;
            }

            if (popupOverlay.parentNode !== document.body) {
                document.body.appendChild(popupOverlay);
            }

            popupOverlay.style.position = 'fixed';
            popupOverlay.style.inset = '0';
            popupOverlay.style.zIndex = '2147483647';
            var previousBodyOverflow = '';

            function openPopup() {
                popupOverlay.classList.remove('hidden');
                popupOverlay.classList.add('flex');
                previousBodyOverflow = document.body.style.overflow;
                document.body.style.overflow = 'hidden';
            }

            function closePopup() {
                popupOverlay.classList.add('hidden');
                popupOverlay.classList.remove('flex');
                document.body.style.overflow = previousBodyOverflow;
            }

            popupClose.addEventListener('click', closePopup);
            popupOverlay.addEventListener('click', function(event) {
                if (event.target === popupOverlay) {
                    closePopup();
                }
            });

            document.addEventListener('keydown', function(event) {
                if (event.key === 'Escape' && !popupOverlay.classList.contains('hidden')) {
                    closePopup();
                }
            });

